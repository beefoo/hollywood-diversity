  <h2>A Quick Guide For Making Classifications</h2>

  <hr />

  <h4>Don't judge just based on name, image, or prior assumptions.</h4>

  <p>Names like Casey, Jesse, or Morgan can be either gender. Someone may look white or black, but may have a mixed racial background. Bottom line: don't guess or assume anything!</p>

  <h4>Do some research</h4>

  <p>Links to the person's IMDB Page, Wikipedia page, and Google search are provided on each screen. Sometimes the person's family background is listed in their bio or in articles about them. It is very helpful to add a reference to the source you find.</p>

  <h4>When in doubt, skip it or say you are unsure</h4>

  <p>Some people are really hard to find information on such as producers or writers. If you are unsure a person's gender, race, or both, just select <em>I am unsure</em> or simply skip them</p>

  <h4>Someone could be multiple races</h4>

  <p>If someone belongs to more than one racial group, select all that apply.</p>

  <p>Racial groups are adapted from the <a href="http://quickfacts.census.gov/qfd/meta/long_RHI525214.htm" target="_blank">how the U.S. Census Bureau defines them</a>:</p>

  <ul>
    <li><strong>White</strong>: A person having origins in any of the original peoples of Europe, the Middle East, or North Africa </li>
    <li><strong>Black</strong>: A person having origins in any of the Black racial groups of Africa </li>
    <li><strong>Hispanic (Non-White)</strong>: A person who identifies their origin as Hispanic, Latino, or Spanish</li>
    <li><strong>Asian/Pacific Islander</strong>: A person having origins in any of the original peoples of the Far East, Southeast Asia, or the Indian subcontinent, or a person having origins in any of the original peoples of Hawaii, Guam, Samoa, or other Pacific Islands</li>
    <li><strong>American Indian</strong>: A person having origins in any of the original peoples of North and South America and who maintains tribal affiliation or community attachment</li>

  </ul>

